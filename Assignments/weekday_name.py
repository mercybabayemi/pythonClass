user_input = int(input("Enter a number between 1 and 7: "))

if user_input == 1:
    print("Monday")
elif user_input == 2:
    print("Tuesday")
elif user_input == 3:
    print("Wednesday")
elif user_input == 4:
    print("Thursday")
elif user_input == 5:
    print("Friday")
elif user_input == 6:
    println("Saturday")
elif user_input == 7:
    print("Sunday")
else:
    println("Error message")